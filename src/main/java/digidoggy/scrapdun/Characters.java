package digidoggy.scrapdun;

abstract class Characters {

    protected static String characterName ;
    protected static int health = 100;
    protected static String characterWeapon ;
    protected static String armorsHead ;
    protected static String armorBody ;
    protected static String armorLegs ;
    protected static String armorArms ;
    public static int damage = 10;
    protected static int crtDamage = 5;
    protected static String block;
    protected static int defence = 10;


    //getters and setters (


    //constructor for block and hit
    public Characters(String armorsHead, String armorBody, String armorLegs, String armorArms) {
        this.armorsHead = armorsHead;
        this.armorBody = armorBody;
        this.armorLegs = armorLegs;
        this.armorArms = armorArms;
    }

    // constructor for weapon


    // constructor for characteristic


    public Characters(int health, int damage, int crtDamage, int defence) {
        this.characterName = characterName;
        this.health = health;
        this.characterWeapon = characterWeapon;
        this.damage = damage;
        this.crtDamage = crtDamage;
        this.block = block;
        this.defence = defence;
    }

    @Override
    public String toString() {
        return "Characters{" +
                "characterName='" + characterName + '\'' +
                ", health=" + health +
                ", weapon='" + characterWeapon + '\'' +
                ", damage=" + damage +
                ", crtDamage=" + crtDamage +
                ", block='" + block + '\'' +
                ", defence=" + defence +
                '}';
    }

    public int calculateArmorDef(){

        return defence;
    }



}
